#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long		i;
	long		n;

	n = nb;
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n >= 10)
	{
		ft_putnbr_fd((n / 10), fd);
		ft_putnbr_fd((n % 10), fd);
	}
	else
	{
		i = n + '0';
		write(fd, &i, 1);
	}
}
